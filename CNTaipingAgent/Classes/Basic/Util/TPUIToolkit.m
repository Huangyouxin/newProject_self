NSString *  floatStringFromString(NSString *string , int decimalNum){
    if ([string rangeOfString:@"."].location !=NSNotFound) {
        NSRange range = [string rangeOfString:@"."];
        if (string.length - range.location < decimalNum + 1) {
            int index = decimalNum + 1 - (string.length - range.location);
            for (int i = 0; i <index; i++) {
                string = [string stringByAppendingString:@"0"];
            }
        }else{
            string = [string substringToIndex:range.location + 3];
        }
        return string;
    }else{
        string = [string stringByAppendingString:@"."];
        for (int i = 0; i <decimalNum; i++) {
            string = [string stringByAppendingString:@"0"];
        }
        return string;
    }
}

