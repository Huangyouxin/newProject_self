//前一天日期
NSDate * preDayForDate(NSDate *currentDate){
    NSDate* calculatedDate;
    
    NSCalendar *gregorian = [[NSCalendar alloc]
                             initWithCalendarIdentifier:NSGregorianCalendar];
    
    NSCalendarUnit unitFlags = NSYearCalendarUnit|NSMonthCalendarUnit|NSDayCalendarUnit;
    
    NSDateComponents *dateComponents = [gregorian components:unitFlags fromDate:currentDate];
    if (dateComponents.day == 1) {
        if (dateComponents.month == 1) {
            dateComponents.year -= 1;
            dateComponents.month = 12;
        }else{
            dateComponents.month -= 1;
        }
        switch (dateComponents.month) {
            case 1:case 3:case 5:case 7:case 8:case 10:case 12:
                dateComponents.day = 31;
                break;
            case 4:case 6:case 9:case 11:
                dateComponents.day = 30;
                break;
            case 2:
                if (0 == dateComponents.year % 4 && 0 != dateComponents.year % 400) {//闰年
                    dateComponents.day = 29;
                }else{//平年
                    dateComponents.day = 28;
                }
                break;
            default:
                break;
        }
    }else{
        dateComponents.day -= 1;
    }
    
    calculatedDate = [gregorian dateFromComponents:dateComponents];
    
    return calculatedDate;
}
